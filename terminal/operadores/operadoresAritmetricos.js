/*
Operadores mais utilizados em JavaScript:
1) Operadores Aritméticos:
 
+) Adição
-) Subtração
*) Multiplicação
/) Divisão
**) Exponenciação 
%) Módulo
++) Incrimento
--) Decremento
*/

// Adição
// Soma
let n1 = 10
let n2 = 25
console.log(n1+n2);

// Subtração
n1 = 10
n2 = 65
console.log(n1 - n2);

// Multiplicação
n1 = 23
n2 = 1000
console.log(n1 * n2)

// Concatenar valor
n1 = 10
n2 = "25"
n3 = 20
console.log(n1 + n2 + n3);

// Divisão 
n1 = 10
n2 = 4
console.log(n1/ n2);

// Módulo
console.log(n1 % n2);

// Incremento
let x = 0;
console.log(x)
x = x + 5;
// ++x  // Pré Incremento
// x++ // Pós Incremento
let y = 1
console.log(y + ++x);






